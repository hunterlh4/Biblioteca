package Entidad;

public class EMaterialLibro {
	private int Id;
	private int LibroId;
	private int MateriaId;
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public int getLibroId() {
		return LibroId;
	}
	public void setLibroId(int libroId) {
		LibroId = libroId;
	}
	public int getMateriaId() {
		return MateriaId;
	}
	public void setMateriaId(int materiaId) {
		MateriaId = materiaId;
	}
	
	
}
