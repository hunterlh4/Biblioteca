package Entidad;

public class EPermiso {

	
	private int Id;
	private int RoldId;
	private int ModuloId;
	private int AccionId;
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public int getRoldId() {
		return RoldId;
	}
	public void setRoldId(int roldId) {
		RoldId = roldId;
	}
	public int getModuloId() {
		return ModuloId;
	}
	public void setModuloId(int moduloId) {
		ModuloId = moduloId;
	}
	public int getAccionId() {
		return AccionId;
	}
	public void setAccionId(int accionId) {
		AccionId = accionId;
	}
	
	
	
}
